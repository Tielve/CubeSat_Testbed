static inline float quat_to_yaw(const float q[4]) {
  float s2 = 2 * (q[0] * q[3] + q[1] * q[2]);
  float c2 = 1 - 2 * (q[2] * q[2] + q[3] * q[3]);
  return atan2f(s2, c2);
}

static inline float wrap_pi(float a) {
  // wrap to (-pi, pi]
  while (a <= -M_PI)
    a += 2.0f * M_PI;
  while (a > M_PI)
    a -= 2.0f * M_PI;
  return a;
}
